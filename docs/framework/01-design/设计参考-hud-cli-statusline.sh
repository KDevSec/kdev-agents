#!/usr/bin/env bash
# ============================================================================
# KDev HUD · 通道① 命令行状态栏 —— 设计参考（kdev-hud 阶段3 / 观测层）
# ----------------------------------------------------------------------------
# 形态：单行 ANSI（truecolor）· 无底色 · 无图标 · 柔和色，作为 Claude Code
#       statusLine 的 command 使用（settings.json → statusLine.type=command）。
#   配置示例：
#     "statusLine": { "type": "command",
#                     "command": "bash <abs-path>/设计参考-hud-cli-statusline.sh" }
# 设计取舍（实测结论）：
#   · 加 ANSI 背景色块 → 在真实 statusLine 里像"贴图/P 图"，违和 → 去底色
#   · emoji 图标在窄状态栏里噪声大、对齐乱 → 去图标
#   · 字号由终端/编辑器字体决定，命令层（ANSI）无法控制
#   · 仅给关键数字（完成度 / 待处理告警）点一抹柔和金，其余走柔和浅灰
# 数据来源（产品形态）：底座事件流派生，只读、不可手改、随发言刷新。
# 配套：同目录 设计参考-hud-dashboard.html（通道② 网页实时仪表盘独立版）。
# ============================================================================

# Claude Code 会把 session JSON 从 stdin 传进来；吞掉，只输出一行
[ -t 0 ] || cat >/dev/null 2>&1

e=$'\033'
R="${e}[0m"
BRAND="${e}[38;2;96;160;240m"   # 柔和品牌蓝
TXT="${e}[38;2;168;182;204m"    # 柔和浅灰（主文案）
GOLD="${e}[38;2;224;168;72m"    # 柔和金（数字 / 告警）
DIM="${e}[38;2;88;102;128m"     # 暗灰分隔线

printf "${BRAND}KDev 团队${DIM} │ ${TXT}需求「用户管理模块」${GOLD}48%%${TXT} · 开发·代码评审 第2轮${DIM} │ ${TXT}开发 忙 · 评审 忙${DIM} │ ${GOLD}待处理 1${R}"
